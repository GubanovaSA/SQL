-- =========================================
-- ЗАДАНИЕ 1 (Анализ на сервере)
-- =========================================

-- Анализируем выполнение запроса без индекса
-- EXPLAIN ANALYZE показывает:
-- 1) План выполнения (какой тип сканирования используется)
-- 2) Реальное время выполнения запроса

EXPLAIN ANALYZE
SELECT *
FROM dealerships
WHERE state = 'TX';

-- =========================================
-- ЗАДАНИЕ 2 (Оптимизация с индексом)
-- =========================================

-- Создаем индекс B-Tree по полю state
-- Индекс ускоряет поиск по условиям WHERE, особенно для =, <, >
CREATE INDEX idx_dealerships_state
ON dealerships(state);

-- Повторно выполняем анализ запроса
EXPLAIN ANALYZE
SELECT *
FROM dealerships
WHERE state = 'TX';

-- Удаляем индекс 
DROP INDEX idx_dealerships_state;

-- =========================================
-- ЗАДАНИЕ 3 (Оптимизация уникального поиска)
-- =========================================

-- Создаем УНИКАЛЬНЫЙ индекс по email
-- UNIQUE индекс:
-- 1) Ускоряет поиск
-- 2) Гарантирует уникальность значений
CREATE UNIQUE INDEX idx_customers_email
ON customers(email);

-- Анализируем выполнение запроса
EXPLAIN ANALYZE
SELECT *
FROM customers
WHERE email = 'test@example.com';
